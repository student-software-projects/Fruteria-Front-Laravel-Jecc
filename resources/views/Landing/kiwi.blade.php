<p>
    El kiwi es la baya de la enredadera Actinidia deliciosa.
    Es originaria de una gran área de China, sobre todo de los bosques del valle del río Yangtsé.
    Introducida en Nueva Zelanda en 1904, fue cultivada desde entonces en muchas regiones templadas por su fruto comestible.
    El nombre «kiwi» le fue otorgado en ese país, posiblemente por una remota similitud de aspecto entre el fruto cubierto de vellosidades y el ave llamada kiwi.
    Es una baya oval de unos 6 cm de largo, con piel delgada de color verde parduzco y densamente cubierta de unos pelillos rígidos y cortos de color marrón. La pulpa,
    firme hasta que madura completamente, es de color verde brillante jugosa y con diminutas semillas negras dispuestas en torno a un corazón blanquecino. Tiene un sabor subácido a bastante ácido, similar al de la grosella o la fresa.1​ Suele ser un alérgeno frecuente, sobre todo su piel
