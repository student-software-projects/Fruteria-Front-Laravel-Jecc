<p>
    Es un fruto con cualidades variables en tamaño, color y firmeza, alargado, generalmente curvado
    y carnoso, rico en almidón cubierto con una cáscara, que puede ser verde, amarilla, roja, púrpura
    o marrón cuando está madura. Los frutos crecen en piñas que cuelgan de la parte superior de la planta.
    Casi todos los plátanos en la actualidad son frutos estériles que no producen semillas fructificantes y
    provienen de dos especies silvestres: Musa acuminata y Musa balbisiana. El nombre científico de la mayoría
    de los plátanos cultivados es Musa × paradisiaca, el híbrido Musa acuminata × M. balbisiana, con distintas denominaciones
    var. o cultivares, dependiendo de su constitución genómica.
