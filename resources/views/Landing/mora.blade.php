    Mora es el nombre que reciben diversos frutos comestibles de distintas especies.

    Las moras son frutas o bayas que, a pesar de proceder de especies vegetales que son completamente diferentes,
    poseen aspecto similar y características comunes. En ocasiones, las distintas moras pueden ser confundidas e incluso obviadas, dado que al usar la palabra mora para hablar de dicha fruta, puede hacerse referencia, simplificando, a dos tipos de bayas procedentes de dos géneros distintos de vegetales con rasgos fenotípicos muy dispares entre sí, el género Morus y Rubus.1​2​Ambos géneros dan moras, pero no son la misma fruta, unas vienen de unos árboles comúnmente llamados moreras y morales (que son del género Morus), y las otras provienen de unas plantas sarmentosas y espinosas comúnmente llamadas zarzas (que son del género Rubus). No obstante, la diferenciación es en realidad aún mayor, dado que dentro de ambos géneros hay un importante número de especies distintas. En total existen más de 300 especies de moras diferentes.3​ Además, la comercialización de estas bayas ha propiciado la creación de diversas hibridaciones que no existen en la naturaleza
