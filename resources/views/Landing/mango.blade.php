<p>
    El mango procede originariamente del noroeste de la India y del norte de Birmania. Se cultivó por primera vez hace aproximadamente cinco mil años. Afortunadamente, el cultivo de los mangos se ha extendido a todas las áreas tropicales y subtropicales del mundo, siendo México el principal país exportador del mundo.

    Gracias a su sabor peculiar y exquisito, es considerado el rey de las frutas tropicales.  El árbol puede alcanzar hasta treinta metros de altura, el tronco de corteza gris es recto, cilíndrico y de 75-100 cm. de diametro. Los frutos son ovales, alargados y en forma de riñón o corazón. La piel de la fruta madura adopta una gama de colores que va del verde, amarillo o naranja hasta el rojo intenso. La pulpa es de color amarillo anaranjado, dulce, jugosa y muy sabrosa. Presenta un sabor y una textura muy diferentes al resto de frutas de mesa considerándose la fruta tropical por excelencia.


